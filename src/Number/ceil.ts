/**
 * Returns the smallest integer greater than or equal to the number (using `Math.ceil`).
 * @param n The number to ceil.
 *
 * @example
 * ```typescript
 * ceil(1.95); // => 2
 * ceil(2.4); // => 3
 * ceil(3); // => 3
 * ```
 *
 * @see {@link Math.ceil}
 */
const ceil = (n: number): number => Math.ceil(n);

export default ceil;
