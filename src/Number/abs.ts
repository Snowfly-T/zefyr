/**
 * Returns the absolute value of the number (using `Math.abs`).
 * @param n The number to get the absolute value of.
 *
 * @example
 * ```typescript
 * abs(-5); // => 5
 * abs(5); // => 5
 * abs(NaN); // => NaN
 * ```
 *
 * @see {@link Math.abs}
 */
const abs = (n: number): number => Math.abs(n);

export default abs;
