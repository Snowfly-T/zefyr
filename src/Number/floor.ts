/**
 * Returns the greatest integer less than or equal to the number (using `Math.floor`).
 * @param n The number to floor.
 *
 * @example
 * ```typescript
 * floor(1.95); // => 1
 * floor(2.4); // => 2
 * floor(3); // => 3
 * ```
 *
 * @see {@link Math.floor}
 */
const floor = (n: number): number => Math.floor(n);

export default floor;
